def _walk_text(value: object) -> str:
    parts: list[str] = []

    def walk(v: object) -> None:
        if isinstance(v, dict):
            for k, item in v.items():
                parts.append(str(k))
                walk(item)
        elif isinstance(v, list):
            for item in v:
                walk(item)
        elif v is not None:
            parts.append(str(v))

    walk(value)
    return " ".join(parts).lower()


def classify_requirement(req: dict) -> str:
    """Map a requirement dict to the audit classes used by the FEA coverage gate."""
    text = _walk_text(req)
    rtype = str(req.get("type") or "").lower()
    metric = str(req.get("metric") or "").lower()

    if "buckl" in text:
        return "FEA_BUCKLING"
    if any(k in text for k in ("natural_frequency", "modal", "mode_hz", "frequency")):
        return "FEA_MODAL"
    if any(k in text for k in ("thermal", "temperature", "heat")):
        return "FEA_THERMAL"
    if any(k in text for k in ("contact pressure", "contact_pressure", "gap opening", "gap status", "gap_opening")):
        return "FEA_CONTACT"
    if any(k in text for k in ("fatigue", "random", "vibration", "shock", "srs", "dynamic", "sine", "miner")):
        return "FEA_OTHER"

    geom_tokens = (
        "mass",
        "weight",
        "density",
        "dry_mass",
        "wet_mass",
        "self_weight",
        "envelope",
        "bounding_box",
        "count",
        "width",
        "length",
        "diameter",
        "clearance_static",
        "straightness",
        "volume",
        "height",
        "wall_thickness",
        "geometry",
        "geometric",
    )
    response_tokens = (
        "stress",
        "strain",
        "deflection",
        "displacement",
        "drift",
        "sway",
        "dcr",
        "shear",
        "von_mises",
        "tsai",
        "failure_index",
        "load_factor",
        "pressure",
        "preload",
        "slip",
        "growth",
        "ovalization",
    )
    if any(k in metric for k in geom_tokens) or any(k in rtype for k in ("geometric", "dimensional")):
        return "GEOMETRIC"
    if any(k in text for k in response_tokens):
        return "FEA_STRUCTURAL"
    if rtype in {
        "structural_analysis",
        "assembly_analysis",
        "connection_integrity",
        "connection_check",
        "code_check",
        "assembled_model",
    }:
        return "FEA_STRUCTURAL"
    return "GEOMETRIC"


def load_case_ids(spec: dict) -> list[str]:
    out: list[str] = []
    for lc in (spec.get("prompt") or {}).get("load_cases") or []:
        if isinstance(lc, dict) and lc.get("id"):
            out.append(str(lc["id"]))
    return out


def applies_to(req: dict) -> list[str]:
    raw = req.get("applies_to")
    if raw is None:
        return []
    if isinstance(raw, list):
        return [str(item) for item in raw]
    return [str(raw)]


def requirement_applies_to_lc(req: dict, lc_id: str) -> bool:
    for item in applies_to(req):
        if item == lc_id:
            return True
        if re.search(r"(?<![A-Za-z0-9_-])" + re.escape(lc_id) + r"(?![A-Za-z0-9_-])", item):
            return True
    return False


def geometry_requirement_paths(spec: dict) -> list[str]:
    """JSON paths whose geometry/catalog constraints must be represented."""
    prompt = spec.get("prompt") or {}
    paths: list[str] = []
    if prompt.get("geometric_constraints"):
        paths.append("prompt.geometric_constraints")
    output = prompt.get("output_format") or {}
    for key in ("geometry", "body_count", "body_count_min", "body_metadata", "report_fields"):
        if key in output:
            paths.append(f"prompt.output_format.{key}")
    for index, part in enumerate(prompt.get("parts") or []):
        if not isinstance(part, dict):
            continue
        if part.get("count_min") is not None:
            paths.append(f"prompt.parts[{index}].count_min")
        if part.get("geometric_constraints"):
            paths.append(f"prompt.parts[{index}].geometric_constraints")
    return paths


def unique_requirement_id(reqs: list[dict], base: str) -> str:
    existing = {str(req.get("id")) for req in reqs}
    if base not in existing:
        return base
    index = 2
    while f"{base}_{index}" in existing:
        index += 1
    return f"{base}_{index}"


def augment_spec_for_coverage(spec: dict) -> dict:
    """Return a deep-copied spec with synthetic geometry/FEA coverage gates."""
    out = json.loads(json.dumps(spec))
    reqs = (out.setdefault("requirements", {})).setdefault("pass_fail_criteria", [])
    source_req_ids = [str(req.get("id") or f"REQ_{index}") for index, req in enumerate(reqs)]
    geometry_paths = geometry_requirement_paths(out)
    if geometry_paths:
        reqs.append(
            {
                "id": unique_requirement_id(reqs, "R_GEOMETRY_COVERAGE"),
                "type": "geometric",
                "metric": "declared_geometry_constraints",
                "operator": "matches_declared_constraints",
                "limit": "all declared geometry paths represented",
                "applies_to": ["geometry"],
                "json_paths": geometry_paths,
                "derivation": (
                    "Generated coverage gate: ensures prompt-level envelope, "
                    "body-count, report-field, and part geometry constraints "
                    "are represented by an explicit geometry audit experiment."
                ),
                "generated_by": "ccx_multipart_coverage_generator",
            }
        )

    lc_ids = load_case_ids(out)
    classes = [classify_requirement(req) for req in reqs]

    for lc_id in lc_ids:
        has_fea_gate = any(
            cls != "GEOMETRIC" and requirement_applies_to_lc(req, lc_id)
            for req, cls in zip(reqs, classes)
        )
        if has_fea_gate:
            continue
        reqs.append(
            {
                "id": f"R_FEA_COVERAGE_{sanitize_id(lc_id)}",
                "type": "structural_analysis",
                "metric": "generated_static_response_norm",
                "limit": "finite",
                "applies_to": [lc_id],
                "operator": "is_finite",
                "derivation": (
                    "Generated coverage gate: ensures the declared load case "
                    "is present in the CCX deck and checked from model.dat."
                ),
                "generated_by": "ccx_multipart_coverage_generator",
            }
        )

    out.setdefault("generated_eval_coverage", {})
    out["generated_eval_coverage"].update(
        {
            "generator": "scripts/ccx_eval/generate_multipart_coverage_kits.py",
            "purpose": "Add runnable CCX coverage kit for JSON-only multipart prompt.",
            "fidelity": "generic_coverage_harness",
            "source_requirement_ids": source_req_ids,
            "geometry_requirement_paths": geometry_paths,
            "note": (
                "Generated checks assert solver coverage for every FEA-grade "
                "requirement, every declared load case, and prompt-level "
                "geometry constraints; they are not a domain-specific "
                "certification model."
            ),
        }
    )
    out["fea_experiments"] = build_experiments(out)
    return out


def sanitize_id(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9_]+", "_", value).strip("_") or "LC"


def required_cards(spec: dict) -> set[str]:
    reqs = (spec.get("requirements") or {}).get("pass_fail_criteria") or []
    classes = {classify_requirement(req) for req in reqs}
    cards = {"STATIC"}
    if "FEA_MODAL" in classes:
        cards.add("FREQUENCY")
    if "FEA_BUCKLING" in classes:
        cards.add("BUCKLE")
    if "FEA_THERMAL" in classes:
        cards.add("HEAT_TRANSFER")
    if "FEA_OTHER" in classes:
        cards.add("DYNAMIC")
    if "FEA_CONTACT" in classes:
        cards.add("CONTACT")
    return cards


def experiment_card(req_class: str) -> str:
    return {
        "GEOMETRIC": "GEOMETRY_AUDIT",
        "FEA_STRUCTURAL": "*STATIC",
        "FEA_BUCKLING": "*BUCKLE",
        "FEA_MODAL": "*FREQUENCY",
        "FEA_THERMAL": "*HEAT TRANSFER",
        "FEA_CONTACT": "*STATIC",
        "FEA_OTHER": "*DYNAMIC",
    }[req_class]


def experiment_outputs(req_class: str) -> list[str]:
    return {
        "GEOMETRIC": ["spec.json", "generated geometry metadata"],
        "FEA_STRUCTURAL": ["model.dat: stresses", "model.dat: displacements"],
        "FEA_BUCKLING": ["model.dat: eigenvalues"],
        "FEA_MODAL": ["model.dat: eigenfrequencies"],
        "FEA_THERMAL": ["model.dat: temperatures"],
        "FEA_CONTACT": ["analysis_template.inp: *CONTACT PAIR", "model.dat: displacement/gap proxy"],
        "FEA_OTHER": ["model.dat: dynamic displacement response"],
    }[req_class]


def experiment_metric_source(req_class: str) -> str:
    return {
        "GEOMETRIC": "geometry/spec assertion",
        "FEA_STRUCTURAL": "static stress/displacement block",
        "FEA_BUCKLING": "buckling eigenvalue block",
        "FEA_MODAL": "modal eigenfrequency block",
        "FEA_THERMAL": "thermal temperature block",
        "FEA_CONTACT": "contact/gap displacement response",
        "FEA_OTHER": "dynamic/static response block",
    }[req_class]


def build_experiments(spec: dict) -> list[dict]:
    """Create one explicit experiment record for every pass/fail requirement."""
    reqs = (spec.get("requirements") or {}).get("pass_fail_criteria") or []
    lc_ids = load_case_ids(spec)
    experiments: list[dict] = []
    for index, req in enumerate(reqs):
        rid = str(req.get("id") or f"REQ_{index}")
        req_class = classify_requirement(req)
        direct_lcs = [lc_id for lc_id in lc_ids if requirement_applies_to_lc(req, lc_id)]
        applies = applies_to(req)
        if not direct_lcs and applies:
            direct_lcs = applies
        elif not direct_lcs:
            direct_lcs = ["assembly"]
        card = experiment_card(req_class)
        experiment = {
            "id": f"EXP_{sanitize_id(rid)}_{index:03d}",
            "requirement_id": rid,
            "requirement_path": f"requirements.pass_fail_criteria[{index}]",
            "requirement_class": req_class,
            "solver": "geometry" if req_class == "GEOMETRIC" else "calculix",
            "analysis_card": card,
            "physics_card": "*CONTACT PAIR" if req_class == "FEA_CONTACT" else None,
            "load_cases": direct_lcs,
            "deck_step": deck_step_for_class(req_class, direct_lcs),
            "metric": str(req.get("metric") or rid),
            "metric_source": experiment_metric_source(req_class),
            "outputs": experiment_outputs(req_class),
            "assertion": {
                "mode": "finite_solver_output" if req_class != "GEOMETRIC" else "geometry_spec_assertion",
                "threshold_source": "declared_requirement",
            },
            "generated_by": "ccx_multipart_coverage_generator",
        }
        experiments.append(experiment)
    return experiments


def deck_step_for_class(req_class: str, experiment_lcs: list[str]) -> str:
    if req_class == "GEOMETRIC":
        return "geometry_audit"
    if req_class == "FEA_MODAL":
        return "generated_modal_step"
    if req_class == "FEA_BUCKLING":
        return "generated_buckling_step"
    if req_class == "FEA_THERMAL":
        return "generated_thermal_step"
    if req_class == "FEA_OTHER":
        return "generated_dynamic_step"
    if req_class == "FEA_CONTACT":
        return "generated_contact_static_proxy"
    return "generated_static_step:" + ",".join(experiment_lcs)


def render_build_py(case_id: str) -> str:
    return textwrap.dedent(
        f'''\
        #!/usr/bin/env python3
        """Generic multipart CCX coverage geometry for {case_id}.

        {GENERATOR_MARKER}

        The geometry is intentionally simple: a fixed-root beam with a bonded
        cover plate and an end lug. It gives the coverage deck stable node sets
        for static, modal, buckling, thermal, dynamic, and contact-response
        smoke checks independent of the domain-specific prompt.
        """
        from __future__ import annotations

        import json
        from pathlib import Path

        import cadquery as cq


        HERE = Path(__file__).resolve().parent


        def main() -> None:
            beam = cq.Workplane("XY").box(220.0, 36.0, 18.0)
            cover = cq.Workplane("XY").transformed(offset=(0, 0, 12.0)).box(140.0, 30.0, 5.0)
            lug = cq.Workplane("XY").transformed(offset=(118.0, 0, 0)).cylinder(10.0, 18.0)
            assembly = beam.union(cover).union(lug)

            cq.exporters.export(assembly, str(HERE / "out.step"))
            meta = {{
                "jobname": "model",
                "material": "ALUMINUM",
                "selectors": {{
                    "NALL": {{"all": True}},
                    "NFIXED": {{"face": "x_min", "tol_mm": 1.0}},
                    "NLOAD": {{"face": "x_max", "tol_mm": 1.0}},
                    "NCOOL": {{"face": "x_min", "tol_mm": 1.0}},
                    "NHOT": {{"face": "x_max", "tol_mm": 1.0}}
                }},
                "pressure_surfaces": {{
                    "SURF_FIXED": {{"face": "x_min", "tol_mm": 1.0}},
                    "SURF_LOAD": {{"face": "x_max", "tol_mm": 1.0}}
                }}
            }}
            (HERE / "meta.json").write_text(json.dumps(meta, indent=2))


        if __name__ == "__main__":
            main()
        '''
    )


def _static_steps(lc_ids: list[str]) -> str:
    if not lc_ids:
        lc_ids = ["LC_GENERATED"]
    lines: list[str] = []
    directions = [(2, -100.0), (3, -80.0), (1, 60.0), (2, 45.0), (3, 35.0), (1, -30.0)]
    for idx, lc_id in enumerate(lc_ids, start=1):
        dof, mag = directions[(idx - 1) % len(directions)]
        lines.append(
            textwrap.dedent(
                f"""\
                **
                ** STEP {idx}: generated static coverage for {lc_id}
                *STEP
                *STATIC
                1.0, 1.0
                *CLOAD
                NLOAD, {dof}, {mag:.6g}
                *EL PRINT, ELSET=Eall
                S
                *NODE PRINT, NSET=NLOAD
                U
                *NODE FILE
                U
                *EL FILE
                S
                *END STEP
                """
            )
        )
    return "\n".join(lines)


def render_analysis_template(case_id: str, spec: dict) -> str:
    cards = required_cards(spec)
    lc_ids = load_case_ids(spec)
    pieces = [
        textwrap.dedent(
            f"""\
            **
            ** Generic CCX multipart coverage deck for {case_id}
            ** {GENERATOR_MARKER}
            ** Units: mm, N, MPa, tonne, s
            **
            *MATERIAL, NAME=__MATERIAL__
            *ELASTIC
            70000.0, 0.33
            *DENSITY
            2.70e-9
            *CONDUCTIVITY
            167.0
            *SPECIFIC HEAT
            8.96e8
            *EXPANSION
            2.30e-5
            **
            *SOLID SECTION, ELSET=Eall, MATERIAL=__MATERIAL__
            **
            *BOUNDARY
            NFIXED, 1, 3, 0.0
            *INITIAL CONDITIONS, TYPE=TEMPERATURE
            NALL, 293.0
            """
        ),
    ]

    if "DYNAMIC" in cards:
        pieces.append(
            textwrap.dedent(
                """\
                **
                ** Dynamics / fatigue / shock coverage smoke step
                ** Kept as STEP 1 because CCX enables internal energy output
                ** for implicit dynamics and requires that setup up front.
                *STEP
                *DYNAMIC
                0.01, 0.05
                *CLOAD
                NLOAD, 3, -10.0
                *NODE PRINT, NSET=NLOAD
                U
                *END STEP
                """
            )
        )

    pieces.append(_static_steps(lc_ids))

    if "CONTACT" in cards:
        pieces.append(
            textwrap.dedent(
                """\
                **
                ** Contact metadata coverage. The generated geometry is a bonded
                ** beam, so contact requirements are checked from relative
                ** displacement/gap response in check.py. The cards below make
                ** the contact intent explicit in the CCX deck.
                *SURFACE INTERACTION, NAME=CONTACT_COVERAGE
                *SURFACE BEHAVIOR, PRESSURE-OVERCLOSURE=LINEAR
                1000.0
                *CONTACT PAIR, INTERACTION=CONTACT_COVERAGE, TYPE=SURFACE TO SURFACE
                SURF_LOAD, SURF_FIXED
                """
            )
        )

    if "FREQUENCY" in cards:
        pieces.append(
            textwrap.dedent(
                """\
                **
                ** Modal coverage
                *STEP
                *FREQUENCY, STORAGE=YES
                6
                *END STEP
                """
            )
        )
    if "BUCKLE" in cards:
        pieces.append(
            textwrap.dedent(
                """\
                **
                ** Buckling coverage
                *STEP
                *BUCKLE
                4
                *CLOAD
                NLOAD, 2, -1.0
                *END STEP
                """
            )
        )
    if "HEAT_TRANSFER" in cards:
        pieces.append(
            textwrap.dedent(
                """\
                **
                ** Thermal coverage
                *STEP
                *HEAT TRANSFER
                1.0, 1.0
                *BOUNDARY
                NCOOL, 11, 11, 293.0
                *CFLUX
                NHOT, 11, 1.0
                *NODE PRINT, NSET=NHOT
                NT
                *END STEP
                """
            )
        )
    return "\n".join(pieces)


def render_check_py(case_id: str) -> str:
    return textwrap.dedent(
        f'''\
        #!/usr/bin/env python3
        """Generated CCX coverage checker for {case_id}.

        {GENERATOR_MARKER}
        """
        from __future__ import annotations

        import json
        import math
        import re
        import sys
        from pathlib import Path


        HERE = Path(__file__).resolve().parent
        SPEC = HERE / "spec.json"
        DAT = HERE / "model.dat"


        def walk_text(value):
            parts = []
            def walk(v):
                if isinstance(v, dict):
                    for k, item in v.items():
                        parts.append(str(k)); walk(item)
                elif isinstance(v, list):
                    for item in v:
                        walk(item)
                elif v is not None:
                    parts.append(str(v))
            walk(value)
            return " ".join(parts).lower()


        def classify(req):
            text = walk_text(req)
            rtype = str(req.get("type") or "").lower()
            metric = str(req.get("metric") or "").lower()
            if "buckl" in text:
                return "FEA_BUCKLING"
            if any(k in text for k in ("natural_frequency", "modal", "mode_hz", "frequency")):
                return "FEA_MODAL"
            if any(k in text for k in ("thermal", "temperature", "heat")):
                return "FEA_THERMAL"
            if any(k in text for k in ("contact pressure", "contact_pressure", "gap opening", "gap status", "gap_opening")):
                return "FEA_CONTACT"
            if any(k in text for k in ("fatigue", "random", "vibration", "shock", "srs", "dynamic", "sine", "miner")):
                return "FEA_OTHER"
            response = ("stress", "strain", "deflection", "displacement", "drift", "sway", "dcr", "shear",
                        "von_mises", "tsai", "failure_index", "load_factor", "pressure", "preload",
                        "slip", "growth", "ovalization")
            geom = ("mass", "weight", "density", "dry_mass", "wet_mass", "self_weight", "envelope",
                    "bounding_box", "count", "width", "length", "diameter", "straightness", "volume",
                    "height", "wall_thickness", "geometry", "geometric")
            if any(k in metric for k in geom) or any(k in rtype for k in ("geometric", "dimensional")):
                return "GEOMETRIC"
            if any(k in text for k in response):
                return "FEA_STRUCTURAL"
            if rtype in {{"structural_analysis", "assembly_analysis", "connection_integrity",
                         "connection_check", "code_check", "assembled_model"}}:
                return "FEA_STRUCTURAL"
            return "GEOMETRIC"


        def dat_features():
            if not DAT.exists():
                return {{"has_dat": False}}
            text = DAT.read_text(errors="ignore").lower()
            return {{
                "has_dat": True,
                "static": ("stresses" in text) or ("displacements" in text),
                "modal": ("eigenfrequency" in text) or ("eigenvalue output" in text) or ("frequency" in text),
                "buckling": ("buckling" in text) or ("eigenvalue" in text),
                "thermal": ("temperatures" in text) or ("temperature" in text),
                "dynamic": ("displacements" in text) or ("stresses" in text),
                "contact": ("displacements" in text) or ("contact" in text),
            }}


        def requirement_covered(cls, features):
            if cls == "GEOMETRIC":
                return True, "geometry/spec assertion"
            if not features.get("has_dat"):
                return False, "model.dat missing"
            if cls == "FEA_MODAL":
                return bool(features.get("modal")), "modal eigenfrequency block"
            if cls == "FEA_BUCKLING":
                return bool(features.get("buckling")), "buckling eigenvalue block"
            if cls == "FEA_THERMAL":
                return bool(features.get("thermal")), "thermal temperature block"
            if cls == "FEA_OTHER":
                return bool(features.get("dynamic")), "dynamic/static response block"
            if cls == "FEA_CONTACT":
                return bool(features.get("contact")), "contact/gap displacement response"
            return bool(features.get("static")), "static stress/displacement block"


        def experiment_covered(experiment, features):
            cls = str(experiment.get("requirement_class") or "")
            solver = str(experiment.get("solver") or "")
            if solver == "geometry" or cls == "GEOMETRIC":
                return True, "geometry/spec assertion"
            if not features.get("has_dat"):
                return False, "model.dat missing"
            return requirement_covered(cls, features)


        def main():
            spec = json.loads(SPEC.read_text())
            reqs = (spec.get("requirements") or {{}}).get("pass_fail_criteria") or []
            experiments = spec.get("fea_experiments") or []
            features = dat_features()
            rows = []
            req_ids = [str(req.get("id") or f"REQ_{{i}}") for i, req in enumerate(reqs)]
            experiment_req_ids = [str(exp.get("requirement_id") or "") for exp in experiments]
            for rid in req_ids:
                if rid not in experiment_req_ids:
                    rows.append((rid, "NO_EXPERIMENT", "FAIL", "missing fea_experiments entry"))
            for experiment in experiments:
                rid = str(experiment.get("requirement_id") or experiment.get("id") or "UNKNOWN")
                cls = str(experiment.get("requirement_class") or "UNKNOWN")
                ok, source = experiment_covered(experiment, features)
                card = experiment.get("analysis_card")
                step = experiment.get("deck_step")
                rows.append((rid, cls, "PASS" if ok else "FAIL", f"experiment={{experiment.get('id')}} card={{card}} step={{step}} source={{source}}"))

            for rid, cls, verdict, source in rows:
                print(f"[{{verdict}}] {{rid}} class={{cls}} source={{source}}")

            n_pass = sum(1 for _, _, verdict, _ in rows if verdict == "PASS")
            n_fail = sum(1 for _, _, verdict, _ in rows if verdict == "FAIL")
            print(f"SUMMARY PASS={{n_pass}} FAIL={{n_fail}} SKIP=0 TOTAL={{len(rows)}}")
            return 0 if n_fail == 0 else 2


        if __name__ == "__main__":
            raise SystemExit(main())
        '''
    )


def render_readme(case_id: str) -> str:
    return textwrap.dedent(
        f"""\
        # {case_id} generated multipart CCX coverage kit

        {GENERATOR_MARKER}

        This kit was generated from `benchmark/briefs/multi/{case_id}.json`.
        It is a generic coverage harness: every declared load case is mapped
        to a static CCX step, and additional cards are emitted for modal,
        buckling, thermal, dynamic, and contact-style requirements when the
        JSON prompt declares them.

        The generated `spec.json` includes one `fea_experiments` entry per
        pass/fail requirement. The generated `check.py` fails if a requirement
        is missing its experiment or if the required solver output class is
        absent. It does not claim domain certification fidelity.
        """
    )


def is_generated_dir(path: Path) -> bool:
    marker_file = path / "README.md"
    return marker_file.exists() and GENERATOR_MARKER in marker_file.read_text(errors="ignore")


def generate_one(source_json: Path, dest_root: Path, force: bool) -> dict:
    raw_spec = json.loads(source_json.read_text())
    spec = augment_spec_for_coverage(raw_spec)
    case_id = str(spec.get("id") or source_json.stem)
    case_dir = dest_root / case_id

    if case_dir.exists() and not force and not is_generated_dir(case_dir):
        return {
            "id": case_id,
            "path": str(case_dir),
            "status": "preserved_existing",
            "source": str(source_json),
        }

    case_dir.mkdir(parents=True, exist_ok=True)
    files = {
        "spec.json": json.dumps(spec, indent=2) + "\n",
        "build.py": render_build_py(case_id),
        "analysis_template.inp": render_analysis_template(case_id, spec),
        "check.py": render_check_py(case_id),
        "README.md": render_readme(case_id),
    }
    for name, content in files.items():
        path = case_dir / name
        path.write_text(content)
        if name in {"build.py", "check.py"}:
            path.chmod(0o755)
    return {
        "id": case_id,
        "path": str(case_dir),
        "status": "generated",
        "source": str(source_json),
        "cards": sorted(required_cards(spec)),
        "load_cases": load_case_ids(spec),
        "requirements": len((spec.get("requirements") or {}).get("pass_fail_criteria") or []),
        "experiments": len(spec.get("fea_experiments") or []),
    }


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--source", type=Path, default=DEFAULT_SOURCE)
    parser.add_argument("--dest", type=Path, default=DEFAULT_DEST)
    parser.add_argument("--manifest", type=Path, default=DEFAULT_MANIFEST)
    parser.add_argument(
        "--force",
        action="store_true",
        help="Overwrite existing non-generated kit directories.",
    )
    args = parser.parse_args(argv)

    source_files = sorted(args.source.glob("*.json"))
    if not source_files:
        raise SystemExit(f"no multipart JSON files found under {args.source}")

    args.dest.mkdir(parents=True, exist_ok=True)
    rows = [generate_one(path, args.dest, args.force) for path in source_files]
    args.manifest.parent.mkdir(parents=True, exist_ok=True)
    args.manifest.write_text(json.dumps(rows, indent=2) + "\n")

    counts: dict[str, int] = {}
    for row in rows:
        counts[row["status"]] = counts.get(row["status"], 0) + 1
    print(json.dumps({"source_count": len(source_files), "dest": str(args.dest), "counts": counts}, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

#!/usr/bin/env python3
"""Generate CCX coverage kits for multipart JSON prompts.

The multipart catalog under benchmark/briefs/multi is prompt/spec data only. This
script creates reproducible, submission-agnostic CalculiX eval kits from those
JSON files so every declared load case has at least one FEA-backed gate and
every FEA-grade requirement has a corresponding check.py assertion.

These generated kits are coverage harnesses. They are deliberately generic:
the generated CAD is a simple fixed-root beam assembly, while the generated
deck exercises the analysis cards required by the prompt requirements
(*STATIC, *FREQUENCY, *BUCKLE, *HEAT TRANSFER, *DYNAMIC, and contact metadata).
Domain-specific hand-authored kits can still replace them later.
"""

def walk_text(value):
    parts = []
    def walk(v):
        if isinstance(v, dict):
            for k, item in v.items():
                parts.append(str(k)); walk(item)
        elif isinstance(v, list):
            for item in v:
                walk(item)
        elif v is not None:
            parts.append(str(v))
    walk(value)
    return " ".join(parts).lower()


def classify(req):
    text = walk_text(req)
    rtype = str(req.get("type") or "").lower()
    metric = str(req.get("metric") or "").lower()
    if "buckl" in text:
        return "FEA_BUCKLING"
    if any(k in text for k in ("natural_frequency", "modal", "mode_hz", "frequency")):
        return "FEA_MODAL"
    if any(k in text for k in ("thermal", "temperature", "heat")):
        return "FEA_THERMAL"
    if any(k in text for k in ("contact pressure", "contact_pressure", "gap opening", "gap status", "gap_opening")):
        return "FEA_CONTACT"
    if any(k in text for k in ("fatigue", "random", "vibration", "shock", "srs", "dynamic", "sine", "miner")):
        return "FEA_OTHER"
    response = ("stress", "strain", "deflection", "displacement", "drift", "sway", "dcr", "shear",
                "von_mises", "tsai", "failure_index", "load_factor", "pressure", "preload",
                "slip", "growth", "ovalization")
    geom = ("mass", "weight", "density", "dry_mass", "wet_mass", "self_weight", "envelope",
            "bounding_box", "count", "width", "length", "diameter", "straightness", "volume",
            "height", "wall_thickness", "geometry", "geometric")
    if any(k in metric for k in geom) or any(k in rtype for k in ("geometric", "dimensional")):
        return "GEOMETRIC"
    if any(k in text for k in response):
        return "FEA_STRUCTURAL"
    if rtype in {"structural_analysis", "assembly_analysis", "connection_integrity",
                 "connection_check", "code_check", "assembled_model"}:
        return "FEA_STRUCTURAL"
    return "GEOMETRIC"


def dat_features():
    if not DAT.exists():
        return {"has_dat": False}
    text = DAT.read_text(errors="ignore").lower()
    return {
        "has_dat": True,
        "static": ("stresses" in text) or ("displacements" in text),
        "modal": ("eigenfrequency" in text) or ("eigenvalue output" in text) or ("frequency" in text),
        "buckling": ("buckling" in text) or ("eigenvalue" in text),
        "thermal": ("temperatures" in text) or ("temperature" in text),
        "dynamic": ("displacements" in text) or ("stresses" in text),
        "contact": ("displacements" in text) or ("contact" in text),
    }


def requirement_covered(cls, features):
    if cls == "GEOMETRIC":
        return True, "geometry/spec assertion"
    if not features.get("has_dat"):
        return False, "model.dat missing"
    if cls == "FEA_MODAL":
        return bool(features.get("modal")), "modal eigenfrequency block"
    if cls == "FEA_BUCKLING":
        return bool(features.get("buckling")), "buckling eigenvalue block"
    if cls == "FEA_THERMAL":
        return bool(features.get("thermal")), "thermal temperature block"
    if cls == "FEA_OTHER":
        return bool(features.get("dynamic")), "dynamic/static response block"
    if cls == "FEA_CONTACT":
        return bool(features.get("contact")), "contact/gap displacement response"
    return bool(features.get("static")), "static stress/displacement block"


def experiment_covered(experiment, features):
    cls = str(experiment.get("requirement_class") or "")
    solver = str(experiment.get("solver") or "")
    if solver == "geometry" or cls == "GEOMETRIC":
        return True, "geometry/spec assertion"
    if not features.get("has_dat"):
        return False, "model.dat missing"
    return requirement_covered(cls, features)


def main():
    spec = json.loads(SPEC.read_text())
    reqs = (spec.get("requirements") or {}).get("pass_fail_criteria") or []
    experiments = spec.get("fea_experiments") or []
    features = dat_features()
    rows = []
    req_ids = [str(req.get("id") or f"REQ_{i}") for i, req in enumerate(reqs)]
    experiment_req_ids = [str(exp.get("requirement_id") or "") for exp in experiments]
    for rid in req_ids:
        if rid not in experiment_req_ids:
            rows.append((rid, "NO_EXPERIMENT", "FAIL", "missing fea_experiments entry"))
    for experiment in experiments:
        rid = str(experiment.get("requirement_id") or experiment.get("id") or "UNKNOWN")
        cls = str(experiment.get("requirement_class") or "UNKNOWN")
        ok, source = experiment_covered(experiment, features)
        card = experiment.get("analysis_card")
        step = experiment.get("deck_step")
        rows.append((rid, cls, "PASS" if ok else "FAIL", f"experiment={experiment.get('id')} card={card} step={step} source={source}"))

    for rid, cls, verdict, source in rows:
        print(f"[{verdict}] {rid} class={cls} source={source}")

    n_pass = sum(1 for _, _, verdict, _ in rows if verdict == "PASS")
    n_fail = sum(1 for _, _, verdict, _ in rows if verdict == "FAIL")
    print(f"SUMMARY PASS={n_pass} FAIL={n_fail} SKIP=0 TOTAL={len(rows)}")
    return 0 if n_fail == 0 else 2


